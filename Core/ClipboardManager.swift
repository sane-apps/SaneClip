    @discardableResult
    func mergeItems(withIDs ids: [UUID], separator: String = "\n") -> ClipboardItem? {
        let textItems = ids.compactMap { id in
            history.first(where: { $0.id == id })
        }
        .compactMap { item -> ClipboardItem? in
            if case .text = item.content { return item }
            return nil
        }

        guard textItems.count >= 2 else { return nil }

        let mergedText = textItems.compactMap { item -> String? in
            if case let .text(text) = item.content { return text }
            return nil
        }
        .joined(separator: separator)

        let allTags = Array(Set(textItems.flatMap(\.tags))).sorted()
        let mergedItem = ClipboardItem(
            content: .text(mergedText),
            sourceAppBundleID: "com.saneclip.app",
            sourceAppName: "SaneClip",
            tags: allTags,
            collection: textItems.first?.collection ?? "Default",
            note: "Merged \(textItems.count) clips"
        )
        addItem(mergedItem)
        return mergedItem
    }

