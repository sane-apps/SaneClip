            dict["tags"] = item.tags
            dict["collection"] = item.collection

            dict["isPinned"] = pinnedIDs.contains(item.id)

            return dict
        }

        return try? JSONSerialization.data(withJSONObject: exportItems, options: .prettyPrinted)
    }

