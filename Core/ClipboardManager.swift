    private static let plainTextTypes: [NSPasteboard.PasteboardType] = [
        .string,
        NSPasteboard.PasteboardType("public.utf8-plain-text"),
        NSPasteboard.PasteboardType("public.utf16-plain-text"),
        NSPasteboard.PasteboardType("public.plain-text")
    ]

    private static let htmlTypes: [NSPasteboard.PasteboardType] = [
        .html,
        NSPasteboard.PasteboardType("NSHTMLPboardType")
    ]

    private static let rtfTypes: [NSPasteboard.PasteboardType] = [
        .rtf,
        NSPasteboard.PasteboardType("NSRTFPboardType")
    ]

    private static func normalizedNonEmptyText(_ raw: String?) -> String? {
        guard let raw else { return nil }
        let normalized = raw
            .replacingOccurrences(of: "\r\n", with: "\n")
            .replacingOccurrences(of: "\r", with: "\n")
        return normalized.trimmingCharacters(in: .whitespacesAndNewlines).isEmpty ? nil : normalized
    }

    private static func decodeStringData(_ data: Data?) -> String? {
        guard let data else { return nil }
        let encodings: [String.Encoding] = [
            .utf8,
            .utf16,
            .utf16LittleEndian,
            .utf16BigEndian,
            .unicode
        ]
        for encoding in encodings {
            if let decoded = String(data: data, encoding: encoding),
               let normalized = normalizedNonEmptyText(decoded) {
                return normalized
            }
        }
        return nil
    }

    private static func plainText(from item: NSPasteboardItem, preferredTypes: [NSPasteboard.PasteboardType]) -> String? {
        for type in preferredTypes {
            if let text = normalizedNonEmptyText(item.string(forType: type)) {
                return text
            }
            if let decoded = decodeStringData(item.data(forType: type)) {
                return decoded
            }
        }

        guard let conformanceType = item.availableType(from: preferredTypes) else { return nil }
        if let text = normalizedNonEmptyText(item.string(forType: conformanceType)) {
            return text
        }
        return decodeStringData(item.data(forType: conformanceType))
    }

    private static func richText(from item: NSPasteboardItem) -> String? {
        for rtfType in rtfTypes {
            if let rtfData = item.data(forType: rtfType),
               let attributed = try? NSAttributedString(
                   data: rtfData,
                   options: [.documentType: NSAttributedString.DocumentType.rtf],
                   documentAttributes: nil
               ),
               let normalized = normalizedNonEmptyText(attributed.string) {
                return normalized
            }
        }

        for htmlType in htmlTypes {
            if let htmlData = item.data(forType: htmlType),
               let attributed = try? NSAttributedString(
                   data: htmlData,
                   options: [
                       .documentType: NSAttributedString.DocumentType.html,
                       .characterEncoding: String.Encoding.utf8.rawValue
                   ],
                   documentAttributes: nil
               ),
               let normalized = normalizedNonEmptyText(attributed.string) {
                return normalized
            }
        }

        return nil
    }

    private static func htmlString(from item: NSPasteboardItem) -> String? {
        for htmlType in htmlTypes {
            if let decoded = decodeStringData(item.data(forType: htmlType)) {
                return decoded
            }
        }
        return nil
    }

    private static func tabularTextFromHTML(_ html: String) -> String? {
        let lower = html.lowercased()
        let looksTabular = lower.contains("<table") ||
            ((lower.contains("<tr") || lower.contains("<tbody")) &&
                (lower.contains("<td") || lower.contains("<th")))
        guard looksTabular else { return nil }

        guard let rowRegex = try? NSRegularExpression(pattern: #"(?is)<tr\b[^>]*>(.*?)</tr>"#),
              let cellRegex = try? NSRegularExpression(pattern: #"(?is)<t[dh]\b[^>]*>(.*?)</t[dh]>"#)
        else { return nil }

        let htmlRange = NSRange(location: 0, length: html.utf16.count)
        let rowMatches = rowRegex.matches(in: html, options: [], range: htmlRange)
        guard !rowMatches.isEmpty else { return nil }

        var lines: [String] = []
        for rowMatch in rowMatches {
            guard rowMatch.numberOfRanges > 1,
                  let rowRange = Range(rowMatch.range(at: 1), in: html)
            else { continue }
            let rowHTML = String(html[rowRange])
            let rowNSRange = NSRange(location: 0, length: rowHTML.utf16.count)
            let cellMatches = cellRegex.matches(in: rowHTML, options: [], range: rowNSRange)

            let rowCells = cellMatches.compactMap { match -> String? in
                guard match.numberOfRanges > 1,
                      let cellRange = Range(match.range(at: 1), in: rowHTML)
                else { return nil }
                return normalizedNonEmptyText(richText(fromHTMLFragment: String(rowHTML[cellRange])))
            }
            .filter { !$0.isEmpty }

            if !rowCells.isEmpty {
                lines.append(rowCells.joined(separator: "\t"))
            }
        }

        let result = lines.joined(separator: "\n")
        guard result.contains("\t") || result.contains("\n") else { return nil }
        return normalizedNonEmptyText(result)
    }

    private static func richText(fromHTMLFragment fragment: String) -> String {
        let wrapped = "<span>\(fragment)</span>"
        guard let data = wrapped.data(using: .utf8),
              let attributed = try? NSAttributedString(
                  data: data,
                  options: [
                      .documentType: NSAttributedString.DocumentType.html,
                      .characterEncoding: String.Encoding.utf8.rawValue
                  ],
                  documentAttributes: nil
              )
        else {
            return fragment
        }
        return attributed.string
    }

    private static func firstTabularHTMLText(from items: [NSPasteboardItem]) -> String? {
        for item in items {
            guard let html = htmlString(from: item),
                  let tabular = tabularTextFromHTML(html)
            else { continue }
            return tabular
        }
        return nil
    }

    private static func firstPlainText(
        from items: [NSPasteboardItem],
        preferredTypes: [NSPasteboard.PasteboardType]
    ) -> String? {
        for item in items {
            if let text = plainText(from: item, preferredTypes: preferredTypes) {
                return text
            }
        }
        return nil
    }

    /// Internal helper used by capture flow and regression tests.
    static func preferredTextForCapture(
        from pasteboardItems: [NSPasteboardItem],
        sourceAppBundleID: String?
    ) -> String? {
        guard !pasteboardItems.isEmpty else { return nil }

        if let tabularText = firstPlainText(from: pasteboardItems, preferredTypes: tabularTextTypes) {
            return tabularText
        }

        if let htmlTabularText = firstTabularHTMLText(from: pasteboardItems) {
            return htmlTabularText
        }

        let multiItemTexts = pasteboardItems.compactMap { plainText(from: $0, preferredTypes: plainTextTypes) }
        if multiItemTexts.count > 1 {
            return normalizedNonEmptyText(multiItemTexts.joined(separator: "\n"))
        }

        if let singleText = multiItemTexts.first,
           singleText.contains("\t") || singleText.contains("\n") {
            return singleText
        }

        if let sourceAppBundleID,
           spreadsheetBundleIDs.contains(sourceAppBundleID),
           let spreadsheetText = firstPlainText(from: pasteboardItems, preferredTypes: plainTextTypes) {
            return spreadsheetText
        }

        return nil
    }

    private static func bestEffortText(from pasteboard: NSPasteboard) -> String? {
        for type in plainTextTypes {
            if let text = normalizedNonEmptyText(pasteboard.string(forType: type)) {
                return text
            }
            if let decoded = decodeStringData(pasteboard.data(forType: type)) {
                return decoded
            }
        }

        guard let items = pasteboard.pasteboardItems else { return nil }
        if let plainFromItems = firstPlainText(from: items, preferredTypes: plainTextTypes) {
            return plainFromItems
        }

        for item in items {
            if let rich = richText(from: item) {
                return rich
            }
        }

        return nil
    }

    private func addTextClipboardItem(
        _ text: String,
        sourceAppBundleID: String?,
        sourceAppName: String?
    ) {
        if !isTextWithinCaptureLimit(text) {
            logger.info("Skipped oversized text clipboard entry (\(text.utf8.count) bytes)")
            return
        }

        let processedString = processString(text)
        if !isTextWithinCaptureLimit(processedString) {
            logger.info("Skipped oversized processed clipboard entry (\(processedString.utf8.count) bytes)")
            return
        }
        lastClipboardContent = processedString
        lastCopyTime = Date()
        addItem(ClipboardItem(
            content: .text(processedString),
            sourceAppBundleID: sourceAppBundleID,
            sourceAppName: sourceAppName
        ))
    }

    private func processClipboardContent(
        _ pasteboard: NSPasteboard, sourceAppBundleID: String?, sourceAppName: String?
    ) {
        // Check if this is a file copy from Finder — Finder puts the file ICON as TIFF
        // on the pasteboard, not the actual file content. Detect file URLs and load the
        // real image if it's an image file.
        if let fileURL = pasteboard.string(forType: .fileURL).flatMap({ URL(string: $0) }),
           fileURL.isFileURL,
           FileManager.default.fileExists(atPath: fileURL.path) {
            let ext = fileURL.pathExtension.lowercased()
            if Self.imageExtensions.contains(ext) {
                let fileSize = (try? FileManager.default.attributesOfItem(atPath: fileURL.path)[.size] as? Int) ?? 0
                if !isImageDataWithinCaptureLimit(fileSize) {
                    logger.warning("Skipping oversized image file (\(fileSize / 1_000_000)MB): \(fileURL.lastPathComponent)")
                } else if let image = NSImage(contentsOf: fileURL) {
                    let size = image.size
                    logger.debug("Captured image file: \(fileURL.lastPathComponent) \(Int(size.width))×\(Int(size.height))")
                    lastClipboardContent = nil
                    lastCopyTime = nil
                    addItem(ClipboardItem(
                        content: .image(image),
                        sourceAppBundleID: sourceAppBundleID,
                        sourceAppName: sourceAppName
                    ))
                    return
                } else {
                    logger.warning("Failed to load image from file: \(fileURL.lastPathComponent)")
                }
                // Fall through to capture file path as text if image load failed
            }
            // Non-image file: fall through to capture file path as text
        }

        if let pasteboardItems = pasteboard.pasteboardItems,
           let preferredText = Self.preferredTextForCapture(
               from: pasteboardItems,
               sourceAppBundleID: sourceAppBundleID
           ) {
            addTextClipboardItem(
                preferredText,
                sourceAppBundleID: sourceAppBundleID,
                sourceAppName: sourceAppName
            )
            return
        }

        // Check for image data on pasteboard (e.g. "Copy Image" in browser)
        let hasImageType = pasteboard.types?.contains(where: {
            [.tiff, .png].contains($0)
        }) ?? false
        let isSpreadsheetSource = sourceAppBundleID.map { Self.spreadsheetBundleIDs.contains($0) } ?? false

        // Only use pasteboard image if there's no file URL (avoids capturing Finder icons)
        let hasFileURL = pasteboard.types?.contains(.fileURL) ?? false
        if hasImageType, !hasFileURL, let image = NSImage(pasteboard: pasteboard) {
            let imageBytes = image.tiffRepresentation?.count ?? 0
            if !isImageDataWithinCaptureLimit(imageBytes) {
                logger.warning("Skipping oversized image pasteboard item (\(imageBytes / 1_000_000)MB)")
                return
            }
            if isSpreadsheetSource {
                let typesSummary = pasteboard.types?.map(\.rawValue).joined(separator: ", ") ?? "unknown"
                logger.info("Spreadsheet clipboard fell back to image capture. Types: \(typesSummary)")
            }
            let size = image.size
            logger.debug("Captured image: \(Int(size.width))×\(Int(size.height))")
            lastClipboardContent = nil
            lastCopyTime = nil
            addItem(ClipboardItem(
                content: .image(image),
                sourceAppBundleID: sourceAppBundleID,
                sourceAppName: sourceAppName
            ))
        } else if let text = Self.bestEffortText(from: pasteboard) {
            addTextClipboardItem(
                text,
                sourceAppBundleID: sourceAppBundleID,
                sourceAppName: sourceAppName
            )
        } else {
            if isSpreadsheetSource {
                let typesSummary = pasteboard.types?.map(\.rawValue).joined(separator: ", ") ?? "unknown"
                logger.warning("Spreadsheet clipboard had no capturable text/image. Types: \(typesSummary)")
            }
            lastClipboardContent = nil
            lastCopyTime = nil
        }
    }

    private func isTextWithinCaptureLimit(_ text: String) -> Bool {
        let limit = SettingsModel.shared.maxCaptureTextBytes
        guard limit > 0 else { return true }
        return text.utf8.count <= limit
    }

    private func isImageDataWithinCaptureLimit(_ bytes: Int) -> Bool {
        let limit = SettingsModel.shared.maxCaptureImageBytes
        guard limit > 0 else { return true }
        return bytes <= limit
    }

