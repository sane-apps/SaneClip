        guard isCapturePaused, let capturePausedUntil else { return nil }
        let remaining = max(0, Int(capturePausedUntil.timeIntervalSinceNow))
        let minutes = remaining / 60
        let seconds = remaining % 60
        if minutes > 0 {
            return "\(minutes)m \(seconds)s"
